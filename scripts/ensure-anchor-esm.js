import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';

const anchorDir = join(process.cwd(), 'node_modules', '@coral-xyz', 'anchor');
const anchorPkgPath = join(anchorDir, 'package.json');
const anchorEsmPath = join(anchorDir, 'anchor-esm.mjs');
const dlmmIndexPath = join(process.cwd(), 'node_modules', '@meteora-ag', 'dlmm', 'dist', 'index.mjs');

if (!existsSync(anchorPkgPath)) {
  console.warn('[postinstall] @coral-xyz/anchor not found, skip anchor ESM fix');
  process.exit(0);
}

const wrapperSource = `// Generated by scripts/ensure-anchor-esm.js
import { createRequire } from 'module';
const require = createRequire(import.meta.url);
const anchor = require('./dist/cjs/index.js');

export const {
  BN,
  web3,
  getProvider,
  setProvider,
  AnchorProvider,
  utils,
  IdlError,
  ProgramErrorStack,
  AnchorError,
  ProgramError,
  LangErrorCode,
  LangErrorMessage,
  translateError,
  BorshInstructionCoder,
  BorshAccountsCoder,
  BorshEventCoder,
  BorshCoder,
  SystemCoder,
  Program,
  parseIdlErrors,
  toInstruction,
  validateAccounts,
  translateAddress,
  splitArgsAndCtx,
  EventManager,
  EventParser,
  AccountClient,
  MethodsBuilderFactory,
  Native,
  workspace,
  Wallet,
} = anchor;

export default anchor;
`;

mkdirSync(dirname(anchorEsmPath), { recursive: true });
if (!existsSync(anchorEsmPath) || readFileSync(anchorEsmPath, 'utf-8') !== wrapperSource) {
  writeFileSync(anchorEsmPath, wrapperSource);
  console.log('[postinstall] ensured @coral-xyz/anchor/anchor-esm.mjs');
}

try {
  const pkg = JSON.parse(readFileSync(anchorPkgPath, 'utf-8'));
  pkg.exports = pkg.exports || {};
  pkg.exports['.'] = pkg.exports['.'] || {};
  pkg.exports['.'].import = './anchor-esm.mjs';
  pkg.exports['.'].require = pkg.exports['.'].require || './dist/cjs/index.js';
  pkg.exports['./anchor-esm.mjs'] = './anchor-esm.mjs';
  pkg.exports['./*'] = pkg.exports['./*'] || './*';
  writeFileSync(anchorPkgPath, JSON.stringify(pkg, null, 2) + '\n');
  console.log('[postinstall] ensured @coral-xyz/anchor package exports');
} catch (error) {
  console.warn('[postinstall] failed to update @coral-xyz/anchor package.json:', error.message);
}

try {
  if (existsSync(dlmmIndexPath)) {
    const current = readFileSync(dlmmIndexPath, 'utf-8');
    const next = current
      .replaceAll('from "@coral-xyz/anchor"', 'from "@coral-xyz/anchor/anchor-esm.mjs"')
      .replaceAll('from "@coral-xyz/anchor/dist/cjs/utils/bytes"', 'from "@coral-xyz/anchor/dist/cjs/utils/bytes/index.js"');
    if (next !== current) {
      writeFileSync(dlmmIndexPath, next);
      console.log('[postinstall] ensured @meteora-ag/dlmm anchor imports');
    }
  }
} catch (error) {
  console.warn('[postinstall] failed to patch @meteora-ag/dlmm:', error.message);
}
